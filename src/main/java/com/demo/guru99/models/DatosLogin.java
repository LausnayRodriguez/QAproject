package com.demo.guru99.models;

public class DatosLogin {
    String usuario;
    String clave;

    public DatosLogin(String usuario, String clave) {
        usuario = usuario;
        clave = clave;
    }

    public String getUsuario() {
        return usuario;
    }

    public String getClave() {
        return clave;
    }


}



